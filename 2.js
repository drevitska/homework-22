// Запросить у пользователя число от 0 до 9 и вывести ему спецсимвол, 
//который расположен на этой клавише (1–!, 2–@, 3–# и т. д).

let key = prompt('Ведитечисло от 0 до 9');
if (key == "1") {
  console.log('!');
}
if (key == "2") {
  console.log('@');
}
if (key == "3") {
  console.log('#');
}
if (key == "4") {
  console.log('$');
}
if (key == "5") {
  console.log('%');
}
if (key == "6") {
  console.log('^');
}
if (key == "7") {
  console.log('&');
}
if (key == "8") {
  console.log('*');
}
if (key == "9") {
  console.log('(');
}
if (key == "0") {
  console.log(')');
}
